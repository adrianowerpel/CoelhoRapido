    private void SalvarCliente(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarCliente
        
        Cliente cliente;
        LoginCliente cliente2;
        
        cliente = new Cliente(TxtCPF.getText(),0,TxtNome.getText(),TxtEndereco.getText(),CbSexo.getSelectedItem().toString(),TxtDataNasc.getText());
        cliente2 = new LoginCliente(TxtCPF.getText(),0,txtSenha.getText());
        
        try {
            if(this.cliente == null)
            {            
                new ClienteDAO().Salvar((Cliente)cliente);            
                new LoginClienteDAO().Salvar((LoginCliente)cliente2);            
            }
            else
            {
                ((Pessoa)cliente).setId(this.cliente.getId());
                ((Login)cliente2).setId(this.cliente2.getId());
                new ClienteDAO().Alterar((Cliente)cliente);
                new LoginClienteDAO().Alterar((LoginCliente)cliente2);
            }
            
            JOptionPane.showMessageDialog(null,"Salvo com sucesso");
            setVisible(false);
            
        } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Erro ao salvar!"+ex.getMessage());
        } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null,"Erro do Driver");
        }
    }//GEN-LAST:event_SalvarCliente

