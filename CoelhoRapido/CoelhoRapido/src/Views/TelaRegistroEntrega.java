    public void PreencherTable(boolean todos) throws HeadlessException, SQLException{
        
        try {
            new PedidosDAO().BuscarPendentes();
            
            if(todos)
            {
                lista = new PedidosDAO().BuscarPendentes();
            }
            
            DefaultTableModel dftm = (DefaultTableModel) TblPedidos.getModel();
            
            while(dftm.getRowCount() >= 1)
            {
                dftm.removeRow(dftm.getRowCount()-1);
            }
            
            for(Pedido p: lista)
            {
                if(p.getStatus().equalsIgnoreCase("Pendente"))
                {
                    String[] linha = {p.getId()+"",p.getCliente().getNome(),p.getCidade().getNome(),p.getVeiculo(),p.getStatus()};
                    dftm.addRow(linha);
                }
            }
            
        } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Erro ao salvar!"+ex.getMessage());
        } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null,"Erro do Driver");
        }
        
    }

