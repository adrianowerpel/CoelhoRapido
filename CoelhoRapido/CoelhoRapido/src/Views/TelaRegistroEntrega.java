    private void RealizarPedido(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RealizarPedido
        
        Pedido pedido;
        
        try {
            
            Motorista m = new MotoristaDAO().BuscarCNH(TxtCNH.getText());
            
            if(CbCidadeAtual.getSelectedItem().toString().equalsIgnoreCase(TxtCidadeDestino.getText()))
            {
                pedido = new Pedido(0,TxtDescricao.getText(),CbCidadeAtual.getSelectedItem().toString(),TxtCarga.getText(),"Entregue",null,Double.parseDouble(TxtValorEntrega.getText()),null,null,m);
                m = new Motorista(m.getCnh(),m.getCategoria(),"Dispon√≠vel",m.getId(),m.getNome(),m.getEndereco(),m.getSexo(),m.getDataNasc());
            }            
            else
            {
                pedido = new Pedido(0,TxtDescricao.getText(),CbCidadeAtual.getSelectedItem().toString(),TxtCarga.getText(),"A Caminho",null,Double.parseDouble(TxtValorEntrega.getText()),null,null,m);
                m = new Motorista(m.getCnh(),m.getCategoria(),"N√£o Dispon√≠vel",m.getId(),m.getNome(),m.getEndereco(),m.getSexo(),m.getDataNasc());
            }
            
                ((Pedido)pedido).setId(Integer.parseInt(TxtCodigoEntrega.getText()));
                new PedidosDAO().Alterar((Pedido)pedido);
                ((Motorista)m).setId(m.getId());
                new MotoristaDAO().Alterar((Motorista)m);
            
            
            JOptionPane.showMessageDialog(null, "Entrega atribuida a CNH!");
            
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
    }//GEN-LAST:event_RealizarPedido

