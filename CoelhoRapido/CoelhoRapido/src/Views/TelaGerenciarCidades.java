    ArrayList<CidadeEntrega> lista;
    
    public void PreencherTable(boolean todos) throws HeadlessException{
        
        try {
            new CidadeEntregaDAO().BuscarTodos();
            
            if(todos)
            {
                lista = new CidadeEntregaDAO().BuscarTodos();
            }
            
            DefaultTableModel dftm = (DefaultTableModel) TblCidades.getModel();
            
            while(dftm.getRowCount() >= 1)
            {
                dftm.removeRow(dftm.getRowCount()-1);
            }
            
            for(CidadeEntrega c: lista)
            {
                String[] linha = {c.getId()+"",c.getNome(),c.getDistancia()+"",c.getTempoEntrega(),c.getValorEntrega()+""};
                dftm.addRow(linha);
            }
            
        } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Erro ao salvar!"+ex.getMessage());
        } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null,"Erro do Driver");
        }
        
    }
    
    
