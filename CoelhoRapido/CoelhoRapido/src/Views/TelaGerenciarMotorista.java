    private void PesquisarMotorista(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PesquisarMotorista
        
        try {
            new MotoristaDAO().BuscarNomeCNH(TxtNome.getText(), TxtCNH.getText());
            
            
                lista = new MotoristaDAO().BuscarNomeCNH(TxtNome.getText(), TxtCNH.getText());
            
            
            DefaultTableModel dftm = (DefaultTableModel) TblMotorista.getModel();
            
            while(dftm.getRowCount() >= 1)
            {
                dftm.removeRow(dftm.getRowCount()-1);
            }
            
            for(Motorista m: lista)
            {
                {
                    String[] linha = {m.getNome(),m.getCnh(),m.getEndereco(),m.getCategoria(),m.getDataNasc(),m.getStatus()};
                    dftm.addRow(linha);
                }
            }
            
        } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Erro ao salvar!"+ex.getMessage());
        } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null,"Erro do Driver");
        }
        
    }//GEN-LAST:event_PesquisarMotorista

    private void AlterarMotorista(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlterarMotorista
        
        if(TblMotorista.getSelectedRow() < 0)
        {
            JOptionPane.showMessageDialog(null,"Selecione um Motorista");
        }
        else
        {
            TelaNovoMotorista tnm = new TelaNovoMotorista(null,true);
            
            Motorista mot = lista.get(TblMotorista.getSelectedRow());
            
            tnm.setMotorista(mot);
            
            tnm.setVisible(true);
            
            try {
                PreencherTable(true);
            } catch (HeadlessException ex) {
                Logger.getLogger(TelaGerenciarMotorista.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(TelaGerenciarMotorista.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_AlterarMotorista

    private void ApagarMotorista(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ApagarMotorista
        
        if(TblMotorista.getSelectedRow() >= 0)
        {
            if(JOptionPane.showConfirmDialog(null,"Deseja Apagar o Motorista Selecionado?","Confirme",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION)
            {
                Motorista m = lista.get(TblMotorista.getSelectedRow());
                
                try 
                {
                    new MotoristaDAO().Apagar(m);
                    PreencherTable(true);
                } 
                catch(SQLException ex)
                {
                    JOptionPane.showMessageDialog(null, ex);
                }
                catch (ClassNotFoundException ex)
                {
                    JOptionPane.showMessageDialog(null, "Erro de Driver");
                }
            }
             
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Selecione um Motorista");
        }
        
    }//GEN-LAST:event_ApagarMotorista

     ArrayList<Motorista> lista;
    
    public void PreencherTable(boolean todos) throws HeadlessException, SQLException{
        
        try {
            new MotoristaDAO().BuscarTodos();
            
            if(todos)
            {
                lista = new MotoristaDAO().BuscarTodos();
            }
            
            DefaultTableModel dftm = (DefaultTableModel) TblMotorista.getModel();
            
            while(dftm.getRowCount() >= 1)
            {
                dftm.removeRow(dftm.getRowCount()-1);
            }
            
            for(Motorista m: lista)
            {
                {
                    String[] linha = {m.getNome(),m.getCnh(),m.getEndereco(),m.getCategoria(),m.getDataNasc(),m.getStatus()};
                    dftm.addRow(linha);
                }
            }
            
        } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Erro ao salvar!"+ex.getMessage());
        } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null,"Erro do Driver");
        }
        
    }
    
